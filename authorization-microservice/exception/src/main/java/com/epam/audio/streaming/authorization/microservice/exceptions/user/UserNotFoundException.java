package com.epam.audio.streaming.authorization.microservice.exceptions.user;

import com.epam.audio.streaming.authorization.microservice.exceptions.EntityNotExistsException;

public class UserNotFoundException extends EntityNotExistsException {
    public UserNotFoundException() {
        super();
    }

    public UserNotFoundException(String message) {
        super(message);
    }

    public UserNotFoundException(String message, Throwable cause) {
        super(message, cause);
    }

    public UserNotFoundException(Throwable cause) {
        super(cause);
    }

    protected UserNotFoundException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
